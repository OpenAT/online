# -*- coding: utf-'8' "-*-"
from openerp import models, fields


class ResUsersSosync(models.Model):
    _name = "res.users"
    _inherit = ["res.users", "base.sosync"]

    # Login name e.g.: "admin" or "max.mustermann@datadialog.net"
    login = fields.Char(sosync="True")

    # Is the user account enabled or disabled (also hidden in view and searches in FSON if active=False)
    active = fields.Boolean(sosync="True")

    # Linked res.partner
    partner_id = fields.Many2one(sosync="True")

    # Access Groups
    groups_id = fields.Many2many(sosync="True")

    # Last Login
    login_date = fields.Date(sosync="True")

    # Fundraising Studio Active Directory Name from addon fso_base
    active_directory_name = fields.Char(sosync="True")

